def _fallback_distilled(log_content: str) -> None:
    """If LLM fails, write a minimal distilled file by extracting structured sections."""
    lines = [
        "# Agent Feedback — Distilled Rules",
        "",
        f"**Fallback mode** (LLM unavailable) | Generated: {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}",
        "",
        "---",
        "",
    ]

    # Extract "Rejected Patterns" and "High-Value Proposal Patterns" sections.
    # The feedback log may contain these sections multiple times (one per run),
    # so collect bullets into ordered lists with a seen-set to dedupe on first-80-chars
    # (absorbs minor phrasing drift). Emit one unified list per section.
    rejected: list[str] = []
    high_value: list[str] = []
    seen_rejected: set[str] = set()
    seen_high: set[str] = set()
    current: str | None = None

    for line in log_content.split("\n"):
        if "## Rejected Patterns" in line:
            current = "rejected"
            continue
        if "## High-Value Proposal Patterns" in line:
            current = "high_value"
            continue
        if line.startswith("## "):
            current = None
            continue
        if current and line[:2].rstrip(".").isdigit() and "." in line[:3]:
            bullet = f"- {line.split('.', 1)[1].strip()}"
            # Dedupe on the bold title — each bullet is `- **Name** — rest`.
            # Absorbs phrasing drift in the trailing explanation between runs.
            if "**" in bullet:
                try:
                    title = bullet.split("**", 2)[1].strip().lower()
                except IndexError:
                    title = bullet[:60].lower()
            else:
                title = bullet[:60].lower()
            if current == "rejected" and title not in seen_rejected:
                seen_rejected.add(title)
                rejected.append(bullet)
            elif current == "high_value" and title not in seen_high:
                seen_high.add(title)
                high_value.append(bullet)

    if rejected:
        lines.append("## ❌ NEVER PROPOSE\n")
        lines.extend(rejected)
    if high_value:
        lines.append("\n## ✅ HIGH-VALUE PATTERNS\n")
        lines.extend(high_value)

    with open(DISTILLED_FILE, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")

    logger.warning("Wrote fallback distilled file (no LLM synthesis)")


# ── Entry point ───────────────────────────────────────────────────────────────

async def consolidate(groq_key: str | None = None) -> bool:
    """
    Main consolidation function.
    Returns True if LLM synthesis succeeded, False if fallback used.
    """
    logger.info("Memory consolidation starting...")

    log_content = _read_feedback_log()
    if not log_content.strip():
        logger.warning("agent-feedback-log.md is empty — nothing to consolidate")
        return False

    entry_count = _count_raw_entries(log_content)
    logger.info(f"Processing {entry_count} raw entries from feedback log")

    # Save episodic snapshot before any modification
    _write_episodic_snapshot(log_content)

    # Try LLM synthesis
    groq_key = groq_key or os.environ.get("GROQ_API_KEY", "")
    synthesis = None

    if groq_key:
        synthesis = await _call_groq_consolidation(log_content, groq_key)

    if synthesis:
        _write_distilled(synthesis)
        logger.success(
            f"Consolidation complete: "
            f"{len(synthesis.get('never_propose', []))} rules, "
            f"{len(synthesis.get('high_value_patterns', []))} patterns, "
            f"{len(synthesis.get('open_findings', []))} open findings"
        )
        return True
    else:
        logger.warning("LLM synthesis failed — using fallback extraction")
        _fallback_distilled(log_content)
        return False


def main() -> None:
    """CLI entry point."""
    import argparse
    from dotenv import load_dotenv
    load_dotenv(project_root / "apps" / "api" / ".env")

    parser = argparse.ArgumentParser(description="Memory consolidation daemon")
    parser.add_argument("--dry-run", action="store_true", help="Print synthesis without writing files")
    args = parser.parse_args()

    if args.dry_run:
        log_content = _read_feedback_log()
        logger.info(f"DRY RUN — {_count_raw_entries(log_content)} entries in log")
        logger.info(f"Would write to: {DISTILLED_FILE}")
        return

    success = asyncio.run(consolidate())
    sys.exit(0 if success else 1)


if __name__ == "__main__":
    main()
