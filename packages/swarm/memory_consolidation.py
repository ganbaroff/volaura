#!/usr/bin/env python3
"""
Memory Consolidation Daemon — биологический sleep-cycle для роя.

Аналог мозга:
  agent-feedback-log.md   = гиппокамп (сырые эпизодические события)
  agent-feedback-distilled.md = неокортекс (дистиллированные правила)
  Этот скрипт = SWS-фаза сна (replay → consolidate → prune)

Что делает:
  1. Читает полный agent-feedback-log.md (вся история)
  2. Использует Groq (бесплатно) для синтеза дистиллированных правил
  3. Записывает agent-feedback-distilled.md (то что агенты ЧИТАЮТ)
  4. Архивирует старые raw-записи из основного лога (REM-прунинг)

Запускается ПОСЛЕ каждого дневного запуска роя в GitHub Actions.
Также можно запустить вручную: python -m packages.swarm.memory_consolidation
"""

