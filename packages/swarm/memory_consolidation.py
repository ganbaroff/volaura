# ── Paths ─────────────────────────────────────────────────────────────────────

SWARM_DIR = project_root / "memory" / "swarm"
FEEDBACK_LOG = SWARM_DIR / "agent-feedback-log.md"
DISTILLED_FILE = SWARM_DIR / "agent-feedback-distilled.md"
EPISODIC_INBOX = SWARM_DIR / "episodic_inbox"

# How many raw log entries to keep in active section before archiving
ACTIVE_WINDOW = 20


# ── Consolidation prompt ──────────────────────────────────────────────────────

CONSOLIDATION_PROMPT = """You are a memory consolidation system for an AI agent swarm working on the Volaura project (verified volunteer platform, Azerbaijan, solo founder Yusif, budget $50/mo).

You will receive the FULL history of agent proposals, decisions, and outcomes.

Your job: synthesize this into 3 compressed, high-signal sections that future agents will read.

INPUT (full feedback log):
{feedback_log}

OUTPUT (JSON only):

{{
  "never_propose": [
    // Hard rules from rejections. Exact proposal types to NEVER repeat.
    // Format: "SHORT RULE — reason (session N evidence)"
    // Max 10 items. Most important first.
  ],
  "high_value_patterns": [
    // Proposal types that got implemented or praised.
    // Format: "PATTERN — why it works (example)"
    // Max 7 items.
  ],
  "open_findings": [
    // Unresolved findings: open bugs, deferred items, dismissed-but-valid risks.
    // Format: "FINDING — status — action needed"
    // Max 10 items. Include items dismissed by CTO that might be valid.
  ],
  "meta_insight": "1-2 sentence insight about the CTO's blind spots or the swarm's systematic errors",
  "entries_processed": <integer count of raw table rows processed>
}}

Rules:
- Be ruthlessly specific. "Don't propose Redis" is good. "Consider infrastructure improvements" is useless.
- Include session numbers in evidence.
- Flag the Session 25 → Session 42 route shadowing pattern: dismissed findings can be critical.
- If you see repeated proposals being rejected, name the pattern explicitly."""


# ── Core ──────────────────────────────────────────────────────────────────────

def _read_feedback_log() -> str:
    """Read the full agent-feedback-log.md."""
    if not FEEDBACK_LOG.exists():
        return ""
    with open(FEEDBACK_LOG, "r", encoding="utf-8") as f:
        return f.read()


def _count_raw_entries(log_content: str) -> int:
    """Count table rows in the raw log."""
    return sum(1 for line in log_content.split("\n") if line.startswith("| ") and not line.startswith("| Session"))


async def _call_groq_consolidation(log_content: str, groq_key: str) -> dict | None:
    """Call Groq to synthesize the log into distilled rules."""
    try:
        from groq import AsyncGroq
        client = AsyncGroq(api_key=groq_key)

        prompt = CONSOLIDATION_PROMPT.format(feedback_log=log_content[:12000])  # ~12k chars fits in context

        resp = await asyncio.wait_for(
            client.chat.completions.create(
                model="llama-3.3-70b-versatile",
                messages=[{"role": "user", "content": prompt}],
                temperature=0.2,  # Low temp for consistency
                max_tokens=2000,
                response_format={"type": "json_object"},
            ),
            timeout=30.0,
        )
        raw = resp.choices[0].message.content or ""
        return json.loads(raw)

    except Exception as e:
        logger.error(f"Groq consolidation failed: {e}")
        return None


def _write_distilled(synthesis: dict) -> None:
    """Write the distilled knowledge file that agents will inject."""
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    entries = synthesis.get("entries_processed", "?")

    lines = [
        "# Agent Feedback — Distilled Rules",
        "",
        f"**Auto-generated by memory_consolidation.py** | Last updated: {now} | Processed {entries} raw entries",
        "**Agents read THIS file, not the full log.** Full log in agent-feedback-log.md (hippocampus).",
        "",
        "---",
        "",
        "## ❌ NEVER PROPOSE (hard rejections)",
        "",
        "These proposal types are permanently rejected. Proposing them again wastes a swarm slot.",
        "",
    ]

    for rule in synthesis.get("never_propose", []):
        lines.append(f"- {rule}")

    lines += [
        "",
        "---",
        "",
        "## ✅ HIGH-VALUE PATTERNS (do more of these)",
        "",
        "These types of proposals consistently get implemented or praised.",
        "",
    ]

    for pattern in synthesis.get("high_value_patterns", []):
        lines.append(f"- {pattern}")

    lines += [
        "",
        "---",
        "",
        "## 🔍 OPEN FINDINGS (unresolved — check before proposing)",
        "",
        "These findings are open, deferred, or dismissed but potentially still valid.",
        "**CRITICAL LESSON:** Session 25 Security Agent found route shadowing, CTO dismissed it.",
        "Session 42: bug was real, P0, lived in production 17 sessions. Check dismissed items.",
        "",
    ]

    for finding in synthesis.get("open_findings", []):
        lines.append(f"- {finding}")

    meta = synthesis.get("meta_insight", "")
    if meta:
        lines += [
            "",
            "---",
            "",
            "## 🧠 Meta-Insight (CTO blind spots / swarm systematic errors)",
            "",
            f"> {meta}",
            "",
        ]

    lines += [
        "---",
        "",
        "**Next consolidation:** Runs automatically after each swarm daily run.",
        "**Manual run:** `python -m packages.swarm.memory_consolidation`",
    ]

    with open(DISTILLED_FILE, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")

    logger.info(f"Distilled file written: {DISTILLED_FILE}")


